function cambiarFondo(elemento) {
    //  console.log("elemento clickeado", elemento);
    if (elemento.textContent == 'Off') {
        elemento.style.backgroundColor = 'green'; // Cambia el texto del botón
        elemento.style.color = 'white'; // Cambia el color de fondo del botón
        elemento.textContent = 'On'
    }
    else {
        elemento.style.backgroundColor = 'Red'
        elemento.textContent = 'Off'
    }
}

